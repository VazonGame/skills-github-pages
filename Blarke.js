// ذخیره کاربران در localStorage
function getUsers() {
    return JSON.parse(localStorage.getItem("users")) || {};
}

// ثبت‌نام کاربر
function register() {
    let username = document.getElementById("username").value;
    let password = document.getElementById("password").value;
    let users = getUsers();

    if (users[username]) {
        document.getElementById("authMessage").innerText = "این نام کاربری قبلاً ثبت شده است!";
    } else {
        users[username] = password;
        localStorage.setItem("users", JSON.stringify(users));
        document.getElementById("authMessage").innerText = "ثبت‌نام موفق! لطفاً وارد شوید.";
    }
}

// ورود کاربر
function login() {
    let username = document.getElementById("username").value;
    let password = document.getElementById("password").value;
    let users = getUsers();

    if (users[username] && users[username] === password) {
        localStorage.setItem("loggedInUser", username);
        document.getElementById("authMessage").innerText = "ورود موفق! حالا می‌توانید از چت‌بات استفاده کنید.";
        document.querySelector(".chat-container").style.display = "block"; // نمایش چت‌بات
        document.querySelector(".auth-container").style.display = "none"; // مخفی کردن فرم ورود
    } else {
        document.getElementById("authMessage").innerText = "نام کاربری یا رمز عبور اشتباه است!";
    }
}